  static List<IslamicFact> get allFacts {
    return _allFacts.map((map) => IslamicFact.fromMap(map)).toList();
  }

  static List<IslamicFact> getRandomFacts(int count) {
    List<IslamicFact> facts = allFacts;
    facts.shuffle();
    return facts.take(count).toList();
  }

  static List<IslamicFact> getFactsByCategory(String category) {
    return _allFacts
        .where((map) => map['category']?.toLowerCase() == category.toLowerCase())
        .map((map) => IslamicFact.fromMap(map))
        .toList();
  }

  static List<String> get allCategories {
    return _allFacts
        .map((fact) => fact['category'] ?? '')
        .where((category) => category.isNotEmpty)
        .toSet()
        .toList();
  }
